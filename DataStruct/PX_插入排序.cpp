int *InsertSort(int a[],int len)
{
	int tmp,i,j;
	for(i=1;i<len;i++)
	{
		tmp=a[i];
		for(j=i;j>0&&a[j-1]>tmp;j--) //这里的a[j-1和tmp决定是否为增序还是逆序 
		{
			a[j]=a[j-1];
		}
		a[j]=tmp;
	}
	return a;
}
void Print(int a[],int len)
{
	for(int i=0;i<len;i++)
	{
		printf("%d->",a[i]);
	}
	printf("\n");
}


